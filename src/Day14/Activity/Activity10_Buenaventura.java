
package Day14.Activity;


public class Activity10_Buenaventura {
    public static void main(String[] args) {
        //       =====Java Math Class======
       
        //  === Math.abs() -   Returns the absolute (positive) value of number
 
         /*
        System.out.println(Math.abs(-25);         
        */      
       
       
         // === Math.max() - Returns the larger of two numbers / Math.max(num1 , num2);
        
         /*
           System.out.println(Math.max(15, 30));
         */
         
         
         // === Math.min() - Returns the smaller of two numbers / Math.min(num1, num2);
         
         /*
         System.out.println(Math.min(15, 30));
         */
         
         
         // === Math.pow() - Raises a number to a specified power / Math.pow(base, exponent);
         
         /*
         System.out.println(Math.pow(2, 3));
         */
         
         
         // === Math.sqrt() - Returns the square root of a number / Math.sqrt(number);
         
        /*
          System.out.println(Math.sqrt(49));
         */
        
        
        // === Math.round() - Rounds a decimal to the nearest whole number / Math.round(number);
        
        /*
        System.out.println(Math.round(6.7));
        */
        
        
        // === Math.ceil() - Rounds a decimal up to the next whole number / Math.ceil(number);
        
        /*
        System.out.println(Math.ceil(99.9));
        */
        
        
        // === Math.floor() - Rounds a decimal down to the previous whole number / Math.floor(number);
        
        /*
        System.out.println(Math.floor(6.9));
        */
        
        // === Math.random() - Generates a random number between 0.0 (inclusive) and 1.0(exclusive) / Math.random();
        
       /*
         System.out.println(Math.random());
        */
       
       
       // === Math.log(); - Returns the natural algorithm (base e) of a number / (Math.log(number);
       
       /*
          System.out.println(Math.log(10));
       */
       
       
       // === Math.log10(); - Returns the algorithm of a number using base 10 / Math.log10(number);
       
       /*
       System.out.println(Math.log10(100));
       */
       
       
       // === Math.exp() - Returns e raised to the given power / Math.exp(number);
       
       /*
       System.out.println(Math.exp(2));
       */
       
       
       // === Math.sin() - Returns the sine of an angle (measured in radians) / Math.sin(angle); 
       
       /*
       System.out.println(Math.sin(Math.PI / 2));
       */
        
       
       // === Math.cos() - Returns the cosine of an angle (in radians) / Math.cos(angle);
       
       /*
       System.out.println(Math.cos(0));
       */
       
       
       // === Math.tan() - Returns the tangent of an angle (in radians) / Math.tan(angle);
       
       System.out.println(Math.tan(Math.PI / 4));
        
             
                   
         
        
        
    }
}
